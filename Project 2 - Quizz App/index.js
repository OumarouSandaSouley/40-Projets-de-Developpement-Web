const questionElement = document.querySelector('#question')
const answers = document.querySelector('#answers')
const btnControl = document.querySelector('#btn-control')
const startBtn  = document.querySelector('#validate-category')
const handleIndex = () =>{
  return Math.floor(Math.random() * 25)
}

let currentQuestionIndex = handleIndex()
let questionNo = 0
let score = 0
let questions = football



const handleCategory = () =>{
    let category = document.querySelector('#categorie').value
    switch (category) {
        case "football":
            questions = football
            break;
        case "volleyball":
            questions = volleyball
            break
        case "basketball":
            questions = basketball
            break
        case "athletisme":
            questions = athletisme
            break
    
        default:
            break;
    }
}


const startQuizz = () =>{
    currentQuestionIndex = handleIndex()
    score =  0
    questionNo = 0
    btnControl.innerHTML = "Suivant"
    handleCategory()
    showQuestion()
}

const showQuestion = () =>{
    resetState()
    let currentQuestion = questions[currentQuestionIndex]
    questionElement.innerHTML = (questionNo+1) + ". " + currentQuestion.question

    currentQuestion.answers.forEach((answer)=>{
        const button  = document.createElement('button')
        button.innerHTML = "¤ "+answer.text
        button.classList.add('answer-element')
        answers.appendChild(button)
        if (answer.correct) {
            button.dataset.correct = answer.correct
        }
        button.addEventListener('click', selectAnswer)
    })
}

const resetState = () =>{
    btnControl.style.display = "none"
    while (answers.firstChild) {
        answers.removeChild(answers.firstChild)
    }
}
const selectAnswer = (e)=>{
    const selectedBtn = e.target
    const isCorrect = selectedBtn.dataset.correct === "true"
    if (isCorrect) {
        selectedBtn.classList.add('correct')
        score++
    } else {
        selectedBtn.classList.add('incorrect')
    }
    Array.from(answers.children).forEach(button=>{
        if (button.dataset.correct === "true") {
            button.classList.add("correct")
        }
        button.disabled = true
    })
    btnControl.style.display = "block"
}


const showScore = () =>{
    resetState()
    questionElement.innerHTML = `Vote score est de ${score} sur 5`
    btnControl.innerHTML = "Jouer encore"
    btnControl.style.display = "block"
}


const handleNextButton = () =>{
    questionNo++
    currentQuestionIndex = handleIndex()
    if (questionNo < 5) {
        showQuestion()
    }
    else{
        showScore()
    }
}


btnControl.addEventListener('click', ()=>{
    if (questionNo < 5) {
        handleNextButton()
    }
    else{
        startQuizz()
    }
})


startBtn.addEventListener('click', ()=>{
  handleCategory()
  startQuizz()
})